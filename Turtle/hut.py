import turtle
x = turtle.Turtle()
x.goto(-100,0)
x.fillcolor("#FF0000")
x.begin_fill()
for i in range(0,4):
  x.forward(100)
  x.right(90)
x.end_fill()

x.left(60)
x.fillcolor("#00FF00")
x.begin_fill()
for i in range(0,3):
  x.forward(100)
  x.right(120)
x.end_fill()


x.penup()
x.forward(100)
x.right(60)
x.pendown()
x.fillcolor("#0000FF")
x.begin_fill()
x.forward(200)
x.right(60)
x.forward(100)
x.right(120)
x.forward(200)
x.end_fill()
x.penup()
x.fillcolor("#000000")
x.begin_fill()
x.left(90)
x.forward(100)
x.left(90)
x.pendown()
x.forward(200)
x.left(90)
x.forward(100)
x.end_fill()

